# Resolved once at import: (char_a, char_b, weight), weight = train replay count.
MATCHUP_PRIOR: tuple[tuple[Character, Character, int], ...] = tuple(
    (Character[a], Character[b], n) for a, b, n in _MATCHUP_COUNTS
)


def matchups_for(n: int) -> list[tuple[Character, Character]]:
    """``n`` deterministic ``(ego_char, opp_char)`` matchups, weight-proportional to
    ``MATCHUP_PRIOR`` and prefix-stable in ``n`` (see module docstring).

    A divisor-method (Sainte-Laguë) heap emits matchups in a fixed order independent
    of ``n``: matchup ``i``'s ``k``-th slot carries priority ``(k + 0.5) / weight_i``,
    and the ``n`` smallest priorities win — so larger weights recur sooner and ``n + 1``
    only appends to ``n``'s result. Each matchup's successive slots alternate ego/opp
    orientation (mirror matchups are orientation-invariant)."""
    if n < 0:
        raise ValueError(f"n must be >= 0, got {n}")
    # (priority, matchup_idx, slot_k); matchup_idx breaks priority ties deterministically.
    heap = [((0.5) / w, i, 0) for i, (_, _, w) in enumerate(MATCHUP_PRIOR)]
    heapq.heapify(heap)
    out: list[tuple[Character, Character]] = []
    for _ in range(n):
        _, i, k = heapq.heappop(heap)
        a, b, w = MATCHUP_PRIOR[i]
        out.append((a, b) if k % 2 == 0 else (b, a))
        heapq.heappush(heap, ((k + 1 + 0.5) / w, i, k + 1))
    return out
